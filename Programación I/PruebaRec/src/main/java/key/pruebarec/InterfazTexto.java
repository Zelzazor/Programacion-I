    public boolean cerrarArchivo(boolean option){

        if ((!"".equals(jTextArea1.getText())||(new File(ruta_guardada)).exists()) || !"".equals(ruta_guardada)){
            LecturaArchivo leer = new LecturaArchivo();
            int input;
            if("".equals(ruta_guardada)){
                input=JOptionPane.showConfirmDialog(null, "¬øDesea guardar los cambios de su archivo?");
                if(input == 0){
                    EscrituraArchivo escribir = new EscrituraArchivo();
                        if(!escribir.buscarArchivo()){
                            return option;
                        }

                    escribir.guardarArchivo(jTextArea1, this);
                    return option;
                    
                }
                if(input == 1){
                    if(option){
                        return true;
                    }
                    else{
                        return true;
                    }

                }
            }
            else if(!leer.getInfoArchivo().equals(jTextArea1.getText())){
                input=JOptionPane.showConfirmDialog(null, "¬øDesea guardar los cambios de su archivo?");
                if(input == 0){
                    EscrituraArchivo escribir = new EscrituraArchivo();
                    if("".equals(ruta_guardada)){
                        escribir.buscarArchivo();
                        
                    }
                    escribir.guardarArchivo(jTextArea1, this);
                    return true;
                }
                if(input == 1){
                    
                    return true;
                    
                }
            }
            else if (option){
                return true;
            }
            else{
                
                return true;
            }
        }     
        
            return "".equals(jTextArea1.getText());
        }

    
     

